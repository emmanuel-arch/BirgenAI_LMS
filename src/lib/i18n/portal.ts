// ─────────────────────────────────────────────────────────────────────────────
// KISWAHILI / ENGLISH — the borrower portal's two voices (blueprint §5.1).
//
// THE DICTIONARY IS CODE, and the TYPE is the completeness check: `SW` is
// declared as `PortalDict` (the shape of `EN`), so a string added to the
// English dictionary without its Kiswahili twin does not lint oddly or fall
// back quietly at runtime — it fails to compile. Same discipline as
// AVAILABLE_FEATURES and the metric catalogue: the platform physically cannot
// ship a screen half-translated, because the gap is a build error, not a QA find.
//
// Values are plain strings with `{placeholder}` slots rendered by `fmt()` —
// strings, not template functions, so a test can walk both trees and assert
// the placeholder sets match key-for-key (a Kiswahili sentence that dropped
// `{amount}` is a bug the type system cannot see; scripts/verify-i18n.ts can).
//
// WHAT IS NOT HERE: the staff console. Officers work in English (the language
// of the audit trail and the regulator); the borrower — who is entitled to
// understand a credit agreement in the language they think in — gets the
// choice. The one console surface that IS translated is the KYC wizard at the
// counter, because the person facing that camera is the customer, not the officer.
// ─────────────────────────────────────────────────────────────────────────────

